import java.util.*;
import java.util.Scanner;

public class MultiplesOfThree {
    public static void main(String[] args)
    {
        for(int i=0; i<6; i++)
        {
            if(i == 3)
            {
                continue;
            }
            else
            {
                System.out.println(i);
            }
        }
    }
}
